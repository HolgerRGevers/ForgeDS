function emitHeader(app: DsApplication): string[] {
  const now = formatDate();
  return [
    "/*",
    " * Generated by : ForgeDS build-ds (TypeScript)",
    ` * Generated on : ${now}`,
    " * Version      : 1.0",
    " */",
    ` application "${app.displayName}"`,
    " {",
    `${indent(1)}date format = "${app.dateFormat}"`,
    `${indent(1)}time zone = "${app.timeZone}"`,
    `${indent(1)}time format = "${app.timeFormat}"`,
  ];
}

function emitForms(
  forms: DsForm[],
  blueprintForms: Map<string, string[]>,
): string[] {
  const lines: string[] = [];
  lines.push(`${indent(1)}forms`);
  lines.push(`\t{`);

  for (const form of forms) {
    lines.push(`\t\tform ${form.linkName}`);
    lines.push(`\t\t{`);
    lines.push(`\t\t\tdisplayname = "${form.displayName}"`);
    lines.push(`\t\t\tsuccess message = "${form.displayName} Added Successfully"`);

    // Section block (required by Zoho)
    lines.push(`\t\t\tSection`);
    lines.push(`\t\t\t(`);
    lines.push(`\t\t\t\ttype = section`);
    lines.push(`\t\t\t\trow = 1`);
    lines.push(`\t\t\t\tcolumn = 0   `);
    lines.push(`\t\t\t\twidth = medium`);
    lines.push(`\t\t\t)`);

    // Fields
    for (const fld of form.fields) {
      const prefix = fld.required ? "must have " : "";
      lines.push(`\t\t\t${prefix}${fld.name}`);
      lines.push(`\t\t\t(`);

      if (fld.fieldType === "Dropdown") {
        lines.push(`\t\t\t\ttype = picklist`);
        lines.push(`\t\t\t\tdisplayname = "${fld.displayName}"`);
        if (fld.choices) {
          lines.push(`\t\t\t\tvalues = ${formatChoices(fld.choices)}`);
        }
      } else {
        lines.push(`\t\t\t\ttype = ${zohoType(fld.fieldType)}`);
        lines.push(`\t\t\t\tdisplayname = "${fld.displayName}"`);
        if (fld.fieldType === "DateTime") {
          lines.push(`\t\t\t\ttimedisplayoptions = "hh:mm:ss"`);
          lines.push(`\t\t\t\talloweddays = 0,1,2,3,4,5,6`);
        } else if (fld.fieldType === "Date") {
          lines.push(`\t\t\t\talloweddays = 0,1,2,3,4,5,6`);
        } else if (fld.fieldType === "Checkbox") {
          lines.push(`\t\t\t\tinitial value = false`);
        } else if (fld.fieldType === "MultiLine") {
          lines.push(`\t\t\t\theight = 100px`);
        }
      }

      lines.push(`\t\t\t\trow = 1`);
      lines.push(`\t\t\t\tcolumn = 1   `);
      lines.push(`\t\t\t\twidth = medium`);
      lines.push(`\t\t\t)`);
    }

    // Actions block (required by Zoho)
    lines.push(`\t`);
    lines.push(`\t\t\tactions`);
    lines.push(`\t\t\t{`);
    lines.push(`\t\t\t\ton add`);
    lines.push(`\t\t\t\t{`);
    lines.push(`\t\t\t\t\tsubmit`);
    lines.push(`\t\t\t\t\t(`);
    lines.push(`\t\t\t\t\t\ttype = submit`);
    lines.push(`\t\t\t\t\t\tdisplayname = "Submit"`);
    lines.push(`\t\t\t\t\t)`);
    lines.push(`\t\t\t\t\treset`);
    lines.push(`\t\t\t\t\t(`);
    lines.push(`\t\t\t\t\t\ttype = reset`);
    lines.push(`\t\t\t\t\t\tdisplayname = "Reset"`);
    lines.push(`\t\t\t\t\t)`);
    lines.push(`\t\t\t\t}`);
    lines.push(`\t\t\t\ton edit`);
    lines.push(`\t\t\t\t{`);
    lines.push(`\t\t\t\t\tupdate`);
    lines.push(`\t\t\t\t\t(`);
    lines.push(`\t\t\t\t\t\ttype = submit`);
    lines.push(`\t\t\t\t\t\tdisplayname = "Update"`);
    lines.push(`\t\t\t\t\t)`);
    lines.push(`\t\t\t\t\tcancel`);
    lines.push(`\t\t\t\t\t(`);
    lines.push(`\t\t\t\t\t\ttype = cancel`);
    lines.push(`\t\t\t\t\t\tdisplayname = "Cancel"`);
    lines.push(`\t\t\t\t\t)`);
    lines.push(`\t\t\t\t}`);
    lines.push(`\t\t\t}`);

    // Blueprint components
    const bpStages = blueprintForms.get(form.linkName);
    if (bpStages) {
      lines.push(`\t\t\tblueprint components`);
      lines.push(`\t\t\t{`);
      const stageStr = bpStages.map((s) => `"${s}"`).join(",");
      lines.push(`\t\t\t\tstages = {${stageStr}}`);
      lines.push(`\t\t\t}`);
    }

    lines.push(`\t\t}`);
  }

  lines.push(`\t}`);
  return lines;
}

function emitReports(reports: DsReport[]): string[] {
  if (reports.length === 0) return [];

  const lines: string[] = [];
  lines.push(`\treports`);
  lines.push(`\t{`);

  for (const rpt of reports) {
    const cols = rpt.columns
      .split(",")
      .map((c) => c.trim())
      .filter(Boolean);

    lines.push(`\t\t${rpt.reportType} ${rpt.linkName}`);
    lines.push(`\t\t{`);
    lines.push(`\t\t\tdisplayName = "${rpt.linkName}"`);
    if (rpt.filter) {
      lines.push(`\t\t\tshow all rows from ${rpt.form}  [${rpt.filter}]  `);
    } else {
      lines.push(`\t\t\tshow all rows from ${rpt.form}    `);
    }
    lines.push(`\t\t\t(`);
    for (const c of cols) {
      lines.push(`\t\t\t\t${c} as "${c}"`);
    }
    lines.push(`\t\t\t)`);
    lines.push(`\t\t}`);
  }

  lines.push(`\t}`);
  return lines;
}

function emitPages(pages: DsPage[]): string[] {
  if (pages.length === 0) return [];

  const lines: string[] = [];
  lines.push(`\tpages`);
  lines.push(`\t{`);

  for (const page of pages) {
    lines.push(`\t\tpage ${page.linkName}`);
    lines.push(`\t\t{`);
    lines.push(`\t\t\tdisplayname = "${page.displayName}"`);
    lines.push(`\t\t\tContent="${page.content}"`);
    lines.push(`\t\t}`);
  }

  lines.push(`\t}`);
  return lines;
}

function emitBlueprints(blueprints: DsBlueprint[]): string[] {
  if (blueprints.length === 0) return [];

  const lines: string[] = [];
  lines.push(`\t\tblueprint`);
  lines.push(`\t\t{`);

  for (const bp of blueprints) {
    const stageDisplay = new Map(bp.stages.map((s) => [s.name, s.displayName]));

    lines.push(`    \t\t\t${bp.linkName} as "${bp.displayName}"`);
    lines.push(`    \t\t\t{`);
    lines.push(`        \t\t\ttype = Blueprint`);
    lines.push(`        \t\t\tform = ${bp.form}`);
    lines.push(`        \t\t\tstart`);
    lines.push(`        \t\t\t{`);
    if (bp.stages.length > 0) {
      lines.push(`            \t\t\tstart stage = "${bp.stages[0].displayName}"`);
    }
    lines.push(`        \t\t\t}`);
    lines.push(`        \t\t\tstages`);
    lines.push(`        \t\t\t{`);
    for (const stage of bp.stages) {
      lines.push(`           \t\t\t"${stage.displayName}"`);
      lines.push(`           \t\t\t{`);
      lines.push(`           \t\t\t}`);
    }
    lines.push(`        \t\t\t}`);
    lines.push(`        \t\t\ttransitions`);
    lines.push(`        \t\t\t{`);
    for (const t of bp.transitions) {
      const fromDisplay = stageDisplay.get(t.fromStage) ?? t.fromStage;
      const toDisplay = stageDisplay.get(t.toStage) ?? t.toStage;
      lines.push(`    \t\t\t\t${t.linkName} as "${t.displayName}"`);
      lines.push(`    \t\t\t\t{`);
      lines.push(`         \t\t\t\ttype = normal`);
      lines.push(`         \t\t\t\tfrom stage = "${fromDisplay}"`);
      lines.push(`         \t\t\t\tto stage = "${toDisplay}"`);
      lines.push(`         \t\t\t\tbefore`);
      lines.push(`         \t\t\t\t{`);
      lines.push(`         \t\t\t\t}`);
      lines.push(`         \t\t\t\tafter`);
      lines.push(`         \t\t\t\t{`);
      lines.push(`         \t\t\t\t}`);
      lines.push(`  \t\t\t\t}`);
    }
    lines.push(`        \t\t\t}`);
    lines.push(`    \t\t\t}`);
  }

  lines.push(`\t\t}`);
  return lines;
}

function emitWorkflow(
  workflows: DsFormWorkflow[],
  schedules: DsSchedule[],
  blueprints: DsBlueprint[],
  timeZone: string,
): string[] {
  if (workflows.length === 0 && schedules.length === 0 && blueprints.length === 0) {
    return [];
  }

  const lines: string[] = [];
  lines.push("");
  lines.push(`\t\tworkflow`);
  lines.push(`\t\t{`);

  if (workflows.length > 0) {
    lines.push(`\t\tform`);
    lines.push(`\t\t{`);
    for (const wf of workflows) {
      lines.push(`\t\t\t${wf.linkName} as "${wf.displayName}"`);
      lines.push(`\t\t\t{`);
      lines.push(`\t\t\t\ttype =  form`);
      lines.push(`\t\t\t\tform = ${wf.form}`);
      lines.push(``);
      lines.push(`\t\t\t\trecord event = ${wf.recordEvent}`);
      lines.push(``);
      lines.push(`\t\t\t\t${wf.eventType}`);
      lines.push(`\t\t\t\t{`);
      lines.push(`\t\t\t\t\tactions `);
      lines.push(`\t\t\t\t\t{`);
      lines.push(`\t\t\t\t\t\tcustom deluge script`);
      lines.push(`\t\t\t\t\t\t(`);
      lines.push(indentCode(wf.code, 7));
      lines.push(`\t\t\t\t\t\t)`);
      lines.push(`\t\t\t\t\t}`);
      lines.push(`\t\t\t\t}`);
      lines.push(``);
      lines.push(`\t\t\t}`);
    }
    lines.push(`\t\t}`);
  }

  if (schedules.length > 0) {
    lines.push(``);
    lines.push(`\t\tschedule`);
    lines.push(`\t\t{`);
    for (const sched of schedules) {
      lines.push(`\t\t\t${sched.linkName} as "${sched.displayName}"`);
      lines.push(`\t\t\t{`);
      lines.push(`\t\t\t\ttype =  schedule`);
      lines.push(`\t\t\t\tform = ${sched.form}`);
      lines.push(`\t\t\t\ttime zone = "${timeZone}"`);
      lines.push(`\t\t\t\ton start`);
      lines.push(`\t\t\t\t{`);
      lines.push(`\t\t\t\t\tactions `);
      lines.push(`\t\t\t\t\t{`);
      lines.push(`\t\t\t\t\ton load`);
      lines.push(`\t\t\t\t\t(`);
      lines.push(indentCode(sched.code, 6));
      lines.push(`\t\t\t\t\t)`);
      lines.push(`\t\t\t\t\t}`);
      lines.push(`\t\t\t\t}`);
      lines.push(`\t\t\t}`);
    }
    lines.push(`\t\t}`);
  }

  if (blueprints.length > 0) {
    lines.push(``);
    lines.push(``);
    lines.push(...emitBlueprints(blueprints));
  }

  lines.push(``);
  lines.push(``);
  lines.push(``);
  lines.push(`\t}`);
  return lines;
}

function emitShareSettings(roles: string[]): string[] {
  const lines: string[] = [];
  lines.push(`\tshare_settings`);
  lines.push(`\t{`);

  const profiles: Array<[string, string, string]> = [
    ["Read", "Users_Permissions", "This profile will have read permission for all components\\n"],
    ["Write", "Users_Permissions", "This profile will have write permission for all components\\n"],
    ["Administrator", "Users_Permissions", "This profile will have all the permissions.\\n"],
    ["Developer", "Developer", "Developer Profile\\n"],
    ["Customer", "Customer_Portal", "This is the default profile having only add and view permission.\\n"],
  ];

  for (const [name, type, desc] of profiles) {
    lines.push(`\t\t\t"${name}"`);
    lines.push(`\t\t\t{`);
    lines.push(`\t\t\t\tname = "${name}"`);
    lines.push(`\t\t\t\ttype = ${type}`);
    lines.push(`\t\t\t\tpermissions = {Chat:true, Predefined:true, ApiAccess:true, PIIAccess:true, ePHIAccess:true}`);
    lines.push(`\t\t\t\tdescription = "${desc}"`);
    lines.push(`\t\t\t}`);
  }

  lines.push(`\t\t\troles`);
  lines.push(`\t\t\t{`);
  const roleList = roles.length > 0 ? roles : ["CEO"];
  for (const role of roleList) {
    lines.push(`\t\t\t\t"${role}"`);
    lines.push(`\t\t\t\t{`);
    lines.push(`\t\t\t\t\tdescription = "User belonging to this role can access data of all other users."`);
    lines.push(`\t\t\t\t}`);
  }
  lines.push(`\t\t\t}`);
  lines.push(`\t}`);
  return lines;
}

