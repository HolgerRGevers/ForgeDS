// ============================================================
// Helpers
// ============================================================

const T = "\t";

function indent(depth: number): string {
  return T.repeat(depth);
}

/** Map forms.yaml field type to Zoho .ds type name. */
function zohoType(fieldType: DsFieldType): string {
  return TYPE_MAP[fieldType] ?? fieldType.toLowerCase();
}

/** Convert comma-separated choices to Zoho format: {"A","B","C"}. */
function formatChoices(choices: string): string {
  const items = choices
    .split(",")
    .map((c) => c.trim())
    .filter(Boolean);
  return "{" + items.map((i) => `"${i}"`).join(",") + "}";
}

/**
 * Re-indent Deluge code to the given tab depth.
 * Preserves relative indentation from the original source.
 */
function indentCode(code: string, depth: number): string {
  const lines = code.split("\n");
  const result: string[] = [];
  for (const line of lines) {
    const stripped = line.trim();
    if (!stripped) {
      result.push("");
    } else {
      const originalIndent = line.length - line.trimStart().length;
      const extraTabs = Math.floor(originalIndent / 4);
      result.push(indent(depth + extraTabs) + stripped);
    }
  }
  return result.join("\n");
}

function formatDate(): string {
  const now = new Date();
  const months = [
    "Jan", "Feb", "Mar", "Apr", "May", "Jun",
    "Jul", "Aug", "Sep", "Oct", "Nov", "Dec",
  ];
  const dd = String(now.getDate()).padStart(2, "0");
  const mmm = months[now.getMonth()];
  const yyyy = now.getFullYear();
  const hh = String(now.getHours()).padStart(2, "0");
  const mm = String(now.getMinutes()).padStart(2, "0");
  const ss = String(now.getSeconds()).padStart(2, "0");
  return `${dd}-${mmm}-${yyyy} ${hh}:${mm}:${ss}`;
}

