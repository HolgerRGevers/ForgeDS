/**
 * Generate a complete Zoho Creator .ds file from a typed DsFile object.
 *
 * The output matches the format produced by `forgeds-build-ds` (Python)
 * and can be imported into Zoho Creator via Settings > Import.
 */
export function emitDs(file: DsFile): string {
  const lines: string[] = [];

  // Header + application declaration
  lines.push(...emitHeader(file.application));

  // Build blueprint_forms map
  const blueprintForms = new Map<string, string[]>();
  for (const bp of file.blueprints) {
    blueprintForms.set(bp.form, bp.stages.map((s) => s.displayName));
  }

  // Forms
  lines.push("");
  lines.push(...emitForms(file.forms, blueprintForms));

  // Reports
  if (file.reports.length > 0) {
    lines.push("");
    lines.push(...emitReports(file.reports));
  }

  // Pages
  if (file.pages.length > 0) {
    lines.push("");
    lines.push(...emitPages(file.pages));
  }

  // Workflow (form workflows + schedules + blueprints)
  lines.push(
    ...emitWorkflow(
      file.workflows,
      file.schedules,
      file.blueprints,
      file.application.timeZone,
    ),
  );

  // Share settings
  lines.push(...emitShareSettings(file.roles));

  // Web section
  lines.push(...emitWeb(file.forms, file.reports, file.menuSections, file.pages));

  // Phone & tablet
  lines.push(...emitDevice(file.forms, "phone"));
  lines.push(...emitDevice(file.forms, "tablet"));

  // Translation
  lines.push(...emitTranslation());

  // Close application block
  lines.push("}");

  return lines.join("\n") + "\n";
}
