/**
 * Zoho Creator .ds file emitter.
 *
 * Converts typed DsFile objects into .ds text output that Zoho Creator
 * can import via Settings > Import.
 *
 * Port of forgeds/core/build_ds.py emit functions to TypeScript.
 */

